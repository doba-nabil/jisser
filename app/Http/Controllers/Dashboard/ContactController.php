    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $contact = Contact::find($id);
        $contact->seen = 1;
        $contact->save();
        return view('dashboard.contact.edit', compact('contact'));
    }

